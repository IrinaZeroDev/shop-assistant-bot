def find_topic(text: str) -> Optional[Topic]:
    """Простая rule-based классификация темы по ключевым словам.

    Возвращает тему с наибольшим числом совпавших ключевых слов, либо None,
    если ни одна тема не набрала совпадений (сигнал для off-topic/другое).
    """
    normalized = text.lower()
    best_topic: Optional[Topic] = None
    best_score = 0
    for topic, entry in KNOWLEDGE_BASE.items():
        score = sum(1 for kw in entry.keywords if kw in normalized)
        if score > best_score:
            best_score = score
            best_topic = topic
    return best_topic


def get_answer(topic: Topic) -> Optional[str]:
    entry = KNOWLEDGE_BASE.get(topic)
    return entry.answer if entry else None
